// Delete alert

export function deleteAlert(message: string): Promise<any> {
    return Swal.fire({
        title: "Are you sure?",
        text: message,
        icon: "warning",
        showCancelButton: true,
        confirmButtonColor: "#3085d6",
        cancelButtonColor: "#d33",
        confirmButtonText: "Yes, delete it!"
    });
}

export function modalAlert(message:string, date:string){
  Swal.fire({
    title: message,
    text: date,
    icon: "info"
  });
}